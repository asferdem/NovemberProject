package day18_For_Loop_Intro;

public class Ato_Z {
    public static void main(String[] args) {

        for (char I=65; I<=90 ;++I ){
            System.out.print(I+" ");
        }
        System.out.println();
        System.out.println("=====================================================");
        for (char i=97; i<=122; ++i){
            System.out.print(i+ " ");
        }



    }
}
