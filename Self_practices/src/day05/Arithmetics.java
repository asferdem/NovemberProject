package day05;

public class Arithmetics {
    public static void main(String[] args) {

        int sum1 = 90;
        int sum2 = 46;
        System.out.println(sum1+sum2);
        double celcius = 35.0;
        double fahrenheit = 9*celcius/5+32;

        System.out.println(celcius +" Celcius ="+fahrenheit+" F");

        double kilometer = 50 ;
        double mile = 1.609344;
        double miles = kilometer*mile;
        System.out.println(kilometer+" Km = "+miles+" Miles");



    }


}
