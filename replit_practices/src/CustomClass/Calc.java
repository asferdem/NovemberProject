package CustomClass;

public class Calc {

    int num1;
    int num2;

    public int plus(int num1,int num2){
        this.num1=num1;
        this.num2=num2;
        return num1+num2;

    }
    public int minus(int num1,int num2){
        this.num1=num1;
        this.num2=num2;
        return num1-num2;

    }

}
