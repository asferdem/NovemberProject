package day05_AritmaticOperators;

public class ArithmethicOperator {
    public static void main(String[] args) {
        System.out.println(20+3-2+1);//22
        System.out.println(20*3);
        System.out.println(2+3-1*4);//2+3-4==5-4

        System.out.println(10/3);// 3----int
        System.out.println(10.0/3);// 3.3333------double
        System.out.println(10/3.0);// same
        System.out.println(10.0/3.0); //double

        System.out.println(10/4.0);//2.5
        System.out.println(10 % 4); //10/4 =2    10-(4*2)=2
        System.out.println(30/8);//30-(8*3 )=6

        System.out.println(99%4);// 99-(4*24)= 3
    }



}
