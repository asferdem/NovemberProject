package day18_For_Loop_Intro;

public class For_Loop_Practice {
    public static void main(String[] args) {
        for (int  i=1 ; i<=5 ; ++i){

            System.out.println("Cybertek School");
        }
        for ( int a= 1; a<=10; ++a){
            System.out.print(a+" ");
        }
        System.out.println();
        System.out.println("==================================");

        for (int b=10; b>=1 ; b-- ){
            System.out.print(b+" ");
        }
        System.out.println();








    }

}
