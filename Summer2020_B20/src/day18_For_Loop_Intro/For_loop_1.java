package day18_For_Loop_Intro;

public class For_loop_1 {
    public static void main(String[] args) {


        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("==============================");
        for (int i=0; i<5; i++ ){
            System.out.println("Hello World");
        }

        for (int a=1;a<=50; ++a){
            System.out.println("FATIH ERDEM");
        }





    }
}
