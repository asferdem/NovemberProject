package day12_Switch_Statement_Scanner;

public class Switch {
    public static void main(String[] args) {
        int a = 10;
        switch (a){
            case 5 :
                System.out.println("five");
                break;
            case 1:
                System.out.println("one");
                break;
            default:
                System.out.println("invalid case");
                break;


        }

    }
}
